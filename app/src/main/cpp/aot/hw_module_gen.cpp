std::string HW_ModuleGen::generate_ee_source(const ELF_Analyzer& analyzer) {
    std::ostringstream os;

    os << "#include \"ee_state.h\"\n";
    os << "#include <android/log.h>\n";
    os << "#include <cstring>\n";
    os << "#include <cstdio>\n";
    os << "\n";
    os << "#define LOG_TAG \"EE\"\n";
    os << "#define LOGI(...) __android_log_print(ANDROID_LOG_INFO, LOG_TAG, __VA_ARGS__)\n";
    os << "#define LOGE(...) __android_log_print(ANDROID_LOG_ERROR, LOG_TAG, __VA_ARGS__)\n";
    os << "#define LOGW(...) __android_log_print(ANDROID_LOG_WARN, LOG_TAG, __VA_ARGS__)\n";
    os << "\n";

    os << "void EE_State::reset() {\n";
    os << "    gpr.reset();\n";
    os << "    pc = 0xBFC00000;\n";
    os << "    npc = pc + 4;\n";
    os << "    branch_delay = false;\n";
    os << "    branch_delay_slot = false;\n";
    os << "    branch_target = 0;\n";
    os << "    fpu.reset();\n";
    os << "    memset(cop0, 0, sizeof(cop0));\n";
    os << "    halted = false;\n";
    os << "    exception_pending = false;\n";
    os << "    intc_stat = 0;\n";
    os << "    intc_mask = 0;\n";
    os << "    dmac_stat = 0;\n";
    os << "    dmac_mask = 0;\n";
    os << "    timer_count = 0;\n";
    os << "    timer_target = 0;\n";
    os << "    memset(ram, 0, sizeof(ram));\n";
    os << "    memset(scratchpad, 0, sizeof(scratchpad));\n";
    os << "    memset(bios, 0xFF, sizeof(bios));\n";
    os << "    cache_isolated = false;\n";
    os << "    cache_swapped = false;\n";
    os << "    cycle_count = 0;\n";
    os << "    instruction_count = 0;\n";
    os << "    cop0[COP0_Status] = 0x10000;\n";
    os << "    cop0[COP0_PRId]   = 0x00002E10;\n";
    os << "    cop0[COP0_Config] = 0x17;\n";
    os << "    cop0[COP0_Random] = 0x3F;\n";
    os << "    cop0[COP0_Wired]  = 0;\n";
    os << "    cop0[COP0_Count]  = 0;\n";
    os << "    cop0[COP0_Compare]= 0;\n";
    os << "    LOGI(\"EE reset: PC=0x%08X\", (uint32_t)pc);\n";
    os << "}\n";
    os << "\n";

    os << "uint8_t* EE_State::phys_ptr(uint32_t addr) {\n";
    os << "    addr &= 0x1FFFFFFF;\n";
    os << "    if (addr < EE_RAM_SIZE) return &ram[addr];\n";
    os << "    if (addr >= EE_SCRATCH_BASE && addr < EE_SCRATCH_BASE + EE_SCRATCH_SIZE)\n";
    os << "        return &scratchpad[addr - EE_SCRATCH_BASE];\n";
    os << "    if (addr >= 0x1FC00000 && addr < 0x1FC00000 + EE_BIOS_SIZE)\n";
    os << "        return &bios[addr - 0x1FC00000];\n";
    os << "    if (addr >= 0x80000000 && addr < 0x80000000 + EE_RAM_SIZE)\n";
    os << "        return &ram[addr - 0x80000000];\n";
    os << "    LOGW(\"EE phys_ptr: unmapped 0x%08X\", addr);\n";
    os << "    return nullptr;\n";
    os << "}\n";
    os << "\n";

    os << "uint32_t EE_State::mem_read32(uint32_t addr) {\n";
    os << "    uint8_t* ptr = phys_ptr(addr);\n";
    os << "    if (!ptr) { LOGE(\"EE read32: unmapped 0x%08X\", addr); return 0; }\n";
    os << "    uint32_t val; memcpy(&val, ptr, 4); return val;\n";
    os << "}\n";
    os << "\n";
    os << "uint64_t EE_State::mem_read64(uint32_t addr) {\n";
    os << "    uint8_t* ptr = phys_ptr(addr);\n";
    os << "    if (!ptr) { LOGE(\"EE read64: unmapped 0x%08X\", addr); return 0; }\n";
    os << "    uint64_t val; memcpy(&val, ptr, 8); return val;\n";
    os << "}\n";
    os << "\n";

    os << "void EE_State::mem_write32(uint32_t addr, uint32_t val) {\n";
    os << "    uint8_t* ptr = phys_ptr(addr);\n";
    os << "    if (!ptr) { LOGE(\"EE write32: unmapped 0x%08X\", addr); return; }\n";
    os << "    memcpy(ptr, &val, 4);\n";
    os << "}\n";
    os << "\n";
    os << "void EE_State::mem_write64(uint32_t addr, uint64_t val) {\n";
    os << "    uint8_t* ptr = phys_ptr(addr);\n";
    os << "    if (!ptr) { LOGE(\"EE write64: unmapped 0x%08X\", addr); return; }\n";
    os << "    memcpy(ptr, &val, 8);\n";
    os << "}\n";
    os << "\n";

    os << "void EE_State::intc_update() {\n";
    os << "    uint16_t pending = intc_stat & intc_mask;\n";
    os << "    if (pending) { cop0[COP0_Cause] |= (1u << 10); exception_pending = true; }\n";
    os << "    else { cop0[COP0_Cause] &= ~(1u << 10); exception_pending = false; }\n";
    os << "    uint32_t dma_pending = dmac_stat & dmac_mask;\n";
    os << "    if (dma_pending) { cop0[COP0_Cause] |= (1u << 10); exception_pending = true; }\n";
    os << "}\n";
    os << "\n";
    os << "void EE_State::intc_assert(uint16_t cause) {\n";
    os << "    intc_stat |= cause;\n";
    os << "    intc_update();\n";
    os << "}\n";
    os << "\n";
    os << "bool EE_State::has_pending_interrupt() const {\n";
    os << "    if (!(cop0[COP0_Status] & (1u << 16))) return false;\n";
    os << "    uint8_t im = (cop0[COP0_Status] >> 8) & 0xFF;\n";
    os << "    uint8_t ip = (cop0[COP0_Cause] >> 8) & 0xFF;\n";
    os << "    return (im & ip) != 0;\n";
    os << "}\n";
    os << "\n";

    os << "void EE_State::trigger_exception(uint32_t vector, int cause_code, int excode) {\n";
    os << "    uint32_t status = cop0[COP0_Status];\n";
    os << "    uint32_t cause  = cop0[COP0_Cause];\n";
    os << "    uint32_t epc    = (uint32_t)pc;\n";
    os << "    cause &= ~0x7C;\n";
    os << "    cause |= (excode & 0x1F) << 2;\n";
    os << "    if (branch_delay_slot) { epc -= 4; cause |= (1u << 31); }\n";
    os << "    else { cause &= ~(1u << 31); }\n";
    os << "    status |= (1u << 1);\n";
    os << "    cop0[COP0_Cause]  = cause;\n";
    os << "    cop0[COP0_EPC]    = epc;\n";
    os << "    cop0[COP0_Status] = status;\n";
    os << "    pc = vector;\n";
    os << "    npc = vector + 4;\n";
    os << "    branch_delay_slot = false;\n";
    os << "    LOGI(\"EE exception: vector=0x%08X cause=%d EPC=0x%08X\", vector, cause_code, epc);\n";
    os << "}\n";
    os << "\n";
    os << "void EE_State::return_from_exception() {\n";
    os << "    cop0[COP0_Status] &= ~(1u << 1);\n";
    os << "    pc = cop0[COP0_EPC];\n";
    os << "    npc = pc + 4;\n";
    os << "    branch_delay_slot = false;\n";
    os << "    LOGI(\"EE return from exception: PC=0x%08X\", (uint32_t)pc);\n";
    os << "}\n";
    os << "\n";

    os << "uint32_t EE_State::kernel_entry_point() const {\n";
    os << "    return 0x00100000;\n";
    os << "}\n";
    os << "\n";
    os << "void EE_State::load_bios_kernel_vectors() {\n";
    os << "    LOGI(\"EE: BIOS kernel vectors loaded (PC=0x%08X)\", (uint32_t)pc);\n";
    os << "    LOGI(\"  TLB Refill = 0x%08X\", EE_EXC_TLB_REFILL);\n";
    os << "    LOGI(\"  Interrupt  = 0x%08X\", EE_EXC_INTERRUPT);\n";
    os << "    LOGI(\"  Syscall    = 0x%08X\", EE_EXC_SYSCALL);\n";
    os << "    if (pc < 0xBFC00000 || pc >= 0xBFC00000 + EE_BIOS_SIZE) {\n";
    os << "        pc = 0xBFC00000;\n";
    os << "        npc = pc + 4;\n";
    os << "    }\n";
    os << "    if (bios[0] != 0xFF)\n";
    os << "        LOGI(\"BIOS ROM loaded (first byte: 0x%02X)\", bios[0]);\n";
    os << "    else\n";
    os << "        LOGW(\"BIOS ROM empty - load BIOS file externally\");\n";
    os << "    LOGI(\"EE: Entry point set to 0x%08X\", (uint32_t)pc);\n";
    os << "}\n";

    return os.str();
}

