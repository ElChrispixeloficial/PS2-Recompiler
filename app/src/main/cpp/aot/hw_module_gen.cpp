std::string HW_ModuleGen::generate_dma_header() {
    std::ostringstream os;

    os << "#pragma once\n";
    os << "#include <cstdint>\n";
    os << "#include <cstring>\n";
    os << "#include <functional>\n";
    os << "#include <vector>\n";
    os << "\n";
    os << "// DMA controller - self-contained, no emulator dependencies\n";
    os << "// Generated by HW_ModuleGen for PS2-Recompiler AOT pipeline\n";
    os << "\n";

    os << "enum DMA_Channel : int {\n";
    os << "    DMA_VIF0=0, DMA_VIF1=1, DMA_GIF=2,\n";
    os << "    DMA_FROM_IPU=3, DMA_TO_IPU=4,\n";
    os << "    DMA_SIF0=5, DMA_SIF1=6, DMA_SIF2=7,\n";
    os << "    DMA_FROM_SPR=8, DMA_TO_SPR=9,\n";
    os << "    DMA_CHANNEL_COUNT = 10\n";
    os << "};\n";
    os << "\n";

    os << "static constexpr uint32_t DMA_CH_BASE   = 0x10008000;\n";
    os << "static constexpr uint32_t DMA_CH_STRIDE = 0x10;\n";
    os << "static constexpr uint32_t DMA_DPCR      = 0x1000E000;\n";
    os << "static constexpr uint32_t DMA_DPCR2     = 0x1000F000;\n";
    os << "static constexpr uint32_t DMA_STAT      = 0x1000E010;\n";
    os << "static constexpr uint32_t DMA_STAT2     = 0x1000F020;\n";
    os << "\n";

    os << "struct DMA_Reg {\n";
    os << "    uint32_t madr, qwc, tag, chcr;\n";
    os << "    bool direction()   const { return (chcr & 1) != 0; }\n";
    os << "    bool mode_normal() const { return ((chcr >> 2) & 3) == 0; }\n";
    os << "    bool mode_chain()  const { return ((chcr >> 2) & 3) == 1; }\n";
    os << "    bool mode_inter()  const { return ((chcr >> 2) & 3) == 2; }\n";
    os << "    bool started()     const { return (chcr & (1u << 24)) != 0; }\n";
    os << "    bool tag_irq()     const { return (chcr & (1u << 31)) != 0; }\n";
    os << "    void reset() { madr = 0; qwc = 0; tag = 0; chcr = 0; }\n";
    os << "};\n";
    os << "\n";

    os << "struct DMA_State {\n";
    os << "    DMA_Reg channels[DMA_CHANNEL_COUNT];\n";
    os << "    uint32_t dpcr, dpcr2, dicr, dicr2;\n";
    os << "    std::function<void(int ch, const uint32_t* src, uint32_t words)> on_read;\n";
    os << "    std::function<void(int ch, uint32_t* dst, uint32_t words)> on_write;\n";
    os << "    void reset();\n";
    os << "    void write_register(uint32_t addr, uint32_t val);\n";
    os << "    uint32_t read_register(uint32_t addr);\n";
    os << "    void start_transfer(int ch);\n";
    os << "    void execute_normal(int ch);\n";
    os << "    void execute_chain(int ch);\n";
    os << "    void execute_interleave(int ch);\n";
    os << "    void update_interrupts();\n";
    os << "};\n";

    return os.str();
}

std::string HW_ModuleGen::generate_dma_source() {
    std::ostringstream os;

    os << "#include \"dma_controller.h\"\n";
    os << "#include <android/log.h>\n";
    os << "#include <cstring>\n";
    os << "\n";
    os << "#define LOG_TAG \"DMA\"\n";
    os << "#define LOGI(...) __android_log_print(ANDROID_LOG_INFO, LOG_TAG, __VA_ARGS__)\n";
    os << "#define LOGE(...) __android_log_print(ANDROID_LOG_ERROR, LOG_TAG, __VA_ARGS__)\n";
    os << "#define LOGW(...) __android_log_print(ANDROID_LOG_WARN, LOG_TAG, __VA_ARGS__)\n";
    os << "\n";

    os << "void DMA_State::reset() {\n";
    os << "    for (int i = 0; i < DMA_CHANNEL_COUNT; i++) channels[i].reset();\n";
    os << "    dpcr = 0x00000765;\n";
    os << "    dpcr2 = 0x00000765;\n";
    os << "    dicr = 0;\n";
    os << "    dicr2 = 0;\n";
    os << "    LOGI(\"DMA reset\");\n";
    os << "}\n";
    os << "\n";

    os << "void DMA_State::write_register(uint32_t addr, uint32_t val) {\n";
    os << "    if (addr >= DMA_CH_BASE && addr < DMA_CH_BASE + DMA_CHANNEL_COUNT * DMA_CH_STRIDE) {\n";
    os << "        int ch = (addr - DMA_CH_BASE) / DMA_CH_STRIDE;\n";
    os << "        int off = (addr - DMA_CH_BASE) % DMA_CH_STRIDE;\n";
    os << "        switch (off) {\n";
    os << "        case 0x0: channels[ch].madr = val; break;\n";
    os << "        case 0x4: channels[ch].qwc = val; break;\n";
    os << "        case 0x8: channels[ch].tag = val; break;\n";
    os << "        case 0xC:\n";
    os << "            channels[ch].chcr = val;\n";
    os << "            if (channels[ch].started()) start_transfer(ch);\n";
    os << "            break;\n";
    os << "        }\n";
    os << "        return;\n";
    os << "    }\n";
    os << "    if (addr == DMA_DPCR)  { dpcr = val;  return; }\n";
    os << "    if (addr == DMA_DPCR2) { dpcr2 = val; return; }\n";
    os << "    if (addr == DMA_STAT)  { dicr &= ~val; update_interrupts(); return; }\n";
    os << "    if (addr == DMA_STAT2) { dicr2 &= ~val; update_interrupts(); return; }\n";
    os << "    LOGW(\"DMA write_register: unmapped 0x%08X = 0x%08X\", addr, val);\n";
    os << "}\n";
    os << "\n";

    os << "uint32_t DMA_State::read_register(uint32_t addr) {\n";
    os << "    if (addr >= DMA_CH_BASE && addr < DMA_CH_BASE + DMA_CHANNEL_COUNT * DMA_CH_STRIDE) {\n";
    os << "        int ch = (addr - DMA_CH_BASE) / DMA_CH_STRIDE;\n";
    os << "        int off = (addr - DMA_CH_BASE) % DMA_CH_STRIDE;\n";
    os << "        switch (off) {\n";
    os << "        case 0x0: return channels[ch].madr;\n";
    os << "        case 0x4: return channels[ch].qwc;\n";
    os << "        case 0x8: return channels[ch].tag;\n";
    os << "        case 0xC: return channels[ch].chcr;\n";
    os << "        }\n";
    os << "    }\n";
    os << "    if (addr == DMA_DPCR)  return dpcr;\n";
    os << "    if (addr == DMA_DPCR2) return dpcr2;\n";
    os << "    if (addr == DMA_STAT)  return dicr;\n";
    os << "    if (addr == DMA_STAT2) return dicr2;\n";
    os << "    LOGW(\"DMA read_register: unmapped 0x%08X\", addr);\n";
    os << "    return 0;\n";
    os << "}\n";
    os << "\n";

    os << "void DMA_State::start_transfer(int ch) {\n";
    os << "    if (ch < 0 || ch >= DMA_CHANNEL_COUNT) return;\n";
    os << "    DMA_Reg& reg = channels[ch];\n";
    os << "    LOGI(\"DMA start: ch=%d madr=0x%08X qwc=%d mode=%s\",\n";
    os << "         ch, reg.madr, reg.qwc,\n";
    os << "         reg.mode_chain() ? \"chain\" : reg.mode_inter() ? \"interleave\" : \"normal\");\n";
    os << "    if (reg.mode_chain()) execute_chain(ch);\n";
    os << "    else if (reg.mode_inter()) execute_interleave(ch);\n";
    os << "    else execute_normal(ch);\n";
    os << "    reg.chcr &= ~(1u << 24);\n";
    os << "    update_interrupts();\n";
    os << "}\n";
    os << "\n";

    os << "void DMA_State::execute_normal(int ch) {\n";
    os << "    DMA_Reg& reg = channels[ch];\n";
    os << "    if (reg.qwc == 0) return;\n";
    os << "    uint32_t words = reg.qwc * 4;\n";
    os << "    if (on_read && reg.direction()) {\n";
    os << "        std::vector<uint32_t> buf(words);\n";
    os << "        on_read(ch, buf.data(), words);\n";
    os << "    } else if (on_write && !reg.direction()) {\n";
    os << "        std::vector<uint32_t> buf(words);\n";
    os << "        on_write(ch, buf.data(), words);\n";
    os << "    }\n";
    os << "    reg.madr += words * 4;\n";
    os << "    reg.qwc = 0;\n";
    os << "    LOGI(\"DMA normal: ch=%d transferred %u words\", ch, words);\n";
    os << "}\n";
    os << "\n";

    os << "void DMA_State::execute_chain(int ch) {\n";
    os << "    DMA_Reg& reg = channels[ch];\n";
    os << "    while (reg.madr != 0 && reg.qwc != 0) {\n";
    os << "        uint32_t words = reg.qwc * 4;\n";
    os << "        if (on_read && reg.direction()) {\n";
    os << "            std::vector<uint32_t> buf(words);\n";
    os << "            on_read(ch, buf.data(), words);\n";
    os << "        }\n";
    os << "        reg.madr += words * 4;\n";
    os << "        if (on_read) {\n";
    os << "            std::vector<uint32_t> tag_buf(4);\n";
    os << "            on_read(ch, tag_buf.data(), 4);\n";
    os << "            reg.tag = tag_buf[0];\n";
    os << "            uint32_t next_addr = tag_buf[1] & 0x1FFFFFFF;\n";
    os << "            uint32_t next_qwc  = tag_buf[2] & 0xFFFF;\n";
    os << "            LOGI(\"DMA chain tag: addr=0x%08X qwc=%d\", next_addr, next_qwc);\n";
    os << "            if (next_qwc == 0 && next_addr == 0) break;\n";
    os << "            reg.madr = next_addr;\n";
    os << "            reg.qwc = next_qwc;\n";
    os << "            if (reg.tag_irq()) dicr |= (1u << (24 + ch));\n";
    os << "        } else {\n";
    os << "            break;\n";
    os << "        }\n";
    os << "    }\n";
    os << "    reg.qwc = 0;\n";
    os << "    LOGI(\"DMA chain: ch=%d completed\", ch);\n";
    os << "}\n";
    os << "\n";

    os << "void DMA_State::execute_interleave(int ch) {\n";
    os << "    DMA_Reg& reg = channels[ch];\n";
    os << "    if (reg.qwc == 0) return;\n";
    os << "    uint32_t words = reg.qwc * 4;\n";
    os << "    if (on_read && reg.direction()) {\n";
    os << "        std::vector<uint32_t> buf(words);\n";
    os << "        on_read(ch, buf.data(), words);\n";
    os << "    } else if (on_write && !reg.direction()) {\n";
    os << "        std::vector<uint32_t> buf(words);\n";
    os << "        on_write(ch, buf.data(), words);\n";
    os << "    }\n";
    os << "    reg.madr += words * 4;\n";
    os << "    reg.qwc = 0;\n";
    os << "    LOGI(\"DMA interleave: ch=%d transferred %u words\", ch, words);\n";
    os << "}\n";
    os << "\n";

    os << "void DMA_State::update_interrupts() {\n";
    os << "    uint32_t status = (dicr >> 24) & 0x7F;\n";
    os << "    uint32_t enable = (dicr >> 16) & 0x7F;\n";
    os << "    uint32_t force  = dicr & 0x7F;\n";
    os << "    uint32_t irq = (status | force) & enable;\n";
    os << "    if (irq) dicr |= (1u << 31);\n";
    os << "    else dicr &= ~(1u << 31);\n";
    os << "    LOGI(\"DMA update_interrupts: DICR=0x%08X irq=0x%02X\", dicr, irq);\n";
    os << "}\n";

    return os.str();
}

