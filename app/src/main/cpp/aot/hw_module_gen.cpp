std::string HW_ModuleGen::generate_iop_source() {
    std::ostringstream os;

    os << "#include \"iop_state.h\"\n";
    os << "#include <android/log.h>\n";
    os << "#include <cstring>\n";
    os << "\n";
    os << "#define LOG_TAG \"IOP\"\n";
    os << "#define LOGI(...) __android_log_print(ANDROID_LOG_INFO, LOG_TAG, __VA_ARGS__)\n";
    os << "#define LOGE(...) __android_log_print(ANDROID_LOG_ERROR, LOG_TAG, __VA_ARGS__)\n";
    os << "#define LOGW(...) __android_log_print(ANDROID_LOG_WARN, LOG_TAG, __VA_ARGS__)\n";
    os << "\n";

    os << "void IOP_State::reset() {\n";
    os << "    gpr.reset();\n";
    os << "    pc = 0xBFC00000;\n";
    os << "    npc = pc + 4;\n";
    os << "    halted = false;\n";
    os << "    memset(cop0, 0, sizeof(cop0));\n";
    os << "    memset(ram, 0, sizeof(ram));\n";
    os << "    intc.reset();\n";
    os << "    dmac.reset();\n";
    os << "    sif.reset();\n";
    os << "    memset(timer_count, 0, sizeof(timer_count));\n";
    os << "    memset(timer_target, 0, sizeof(timer_target));\n";
    os << "    memset(timer_mode, 0, sizeof(timer_mode));\n";
    os << "    memset(sio2_fifo, 0, sizeof(sio2_fifo));\n";
    os << "    memset(sio2_recv, 0, sizeof(sio2_recv));\n";
    os << "    sio2_ctrl = 0;\n";
    os << "    sio2_stat = 0;\n";
    os << "    cycle_count = 0;\n";
    os << "    cop0[12] = 0x10000;\n";
    os << "    cop0[15] = 0x00000002;\n";
    os << "    LOGI(\"IOP reset: PC=0x%08X\", pc);\n";
    os << "}\n";
    os << "\n";

    os << "uint8_t* IOP_State::phys_ptr(uint32_t addr) {\n";
    os << "    addr &= 0x1FFFFFFF;\n";
    os << "    if (addr < IOP_RAM_SIZE) return &ram[addr];\n";
    os << "    LOGW(\"IOP phys_ptr: unmapped 0x%08X\", addr);\n";
    os << "    return nullptr;\n";
    os << "}\n";
    os << "\n";

    os << "uint8_t IOP_State::mem_read8(uint32_t addr) {\n";
    os << "    uint8_t* ptr = phys_ptr(addr);\n";
    os << "    return ptr ? *ptr : 0;\n";
    os << "}\n";
    os << "uint16_t IOP_State::mem_read16(uint32_t addr) {\n";
    os << "    uint8_t* ptr = phys_ptr(addr);\n";
    os << "    if (!ptr) return 0;\n";
    os << "    uint16_t val; memcpy(&val, ptr, 2); return val;\n";
    os << "}\n";
    os << "uint32_t IOP_State::mem_read32(uint32_t addr) {\n";
    os << "    uint8_t* ptr = phys_ptr(addr);\n";
    os << "    if (!ptr) return 0;\n";
    os << "    uint32_t val; memcpy(&val, ptr, 4); return val;\n";
    os << "}\n";
    os << "void IOP_State::mem_write8(uint32_t addr, uint8_t val) {\n";
    os << "    uint8_t* ptr = phys_ptr(addr);\n";
    os << "    if (ptr) *ptr = val;\n";
    os << "}\n";
    os << "void IOP_State::mem_write16(uint32_t addr, uint16_t val) {\n";
    os << "    uint8_t* ptr = phys_ptr(addr);\n";
    os << "    if (ptr) memcpy(ptr, &val, 2);\n";
    os << "}\n";
    os << "void IOP_State::mem_write32(uint32_t addr, uint32_t val) {\n";
    os << "    uint8_t* ptr = phys_ptr(addr);\n";
    os << "    if (ptr) memcpy(ptr, &val, 4);\n";
    os << "}\n";
    os << "\n";

    os << "uint32_t IOP_State::io_read32(uint32_t addr) {\n";
    os << "    if (addr == 0x1F801070) return intc.stat;\n";
    os << "    if (addr == 0x1F801074) return intc.mask;\n";
    os << "    if (addr == 0x1F8010F0) return dmac.dpcr;\n";
    os << "    if (addr == 0x1F8010F4) return dmac.dicr;\n";
    os << "    if (addr >= 0x1F801080 && addr < 0x1F8010F0) {\n";
    os << "        int ch = (addr - 0x1F801080) / 0x10;\n";
    os << "        int off = (addr - 0x1F801080) % 0x10;\n";
    os << "        if (ch < IOP_DMAC::CHANNEL_COUNT) {\n";
    os << "            if (off == 0x0) return dmac.channels[ch].base;\n";
    os << "            if (off == 0x4) return dmac.channels[ch].block_control;\n";
    os << "            if (off == 0x8) return dmac.channels[ch].ch_control;\n";
    os << "        }\n";
    os << "        return 0;\n";
    os << "    }\n";
    os << "    if (addr == 0x1F8010C0) return sif.smcom;\n";
    os << "    if (addr == 0x1F8010C4) return sif.mscom;\n";
    os << "    if (addr == 0x1F8010C8) return sif.smgs;\n";
    os << "    if (addr == 0x1F8010CC) return sif.mgs;\n";
    os << "    if (addr == 0x1F8010D0) return sif.sbus_ctl;\n";
    os << "    if (addr == 0x1F8010D4) return sif.sbus_sync;\n";
    os << "    if (addr == 0x1F8010D8) return sif.sbus_cvt;\n";
    os << "    if (addr == 0x1F8010E0) return sif.ifc;\n";
    os << "    if (addr == 0x1F8010E4) return sif.iff;\n";
    os << "    if (addr == 0x1F8010E8) return sif.sif0_msk;\n";
    os << "    if (addr >= 0x1F801100 && addr < 0x1F801110) {\n";
    os << "        int t = (addr - 0x1F801100) / 4;\n";
    os << "        if (t < 3) return timer_count[t];\n";
    os << "    }\n";
    os << "    if (addr >= 0x1F801110 && addr < 0x1F801120) {\n";
    os << "        int t = (addr - 0x1F801110) / 4;\n";
    os << "        if (t < 3) return timer_target[t];\n";
    os << "    }\n";
    os << "    if (addr >= 0x1F801120 && addr < 0x1F801130) {\n";
    os << "        int t = (addr - 0x1F801120) / 4;\n";
    os << "        if (t < 3) return timer_mode[t];\n";
    os << "    }\n";
    os << "    if (addr == 0x1F801040) {\n";
    os << "        if (sio2_stat & 0x04) return sio2_fifo[0];\n";
    os << "        return 0;\n";
    os << "    }\n";
    os << "    if (addr == 0x1F801044) return sio2_stat;\n";
    os << "    if (addr == 0x1F801048) {\n";
    os << "        uint32_t val = sio2_recv[0];\n";
    os << "        memmove(sio2_recv, sio2_recv + 1, sizeof(sio2_recv) - 4);\n";
    os << "        return val;\n";
    os << "    }\n";
    os << "    LOGW(\"IOP io_read32: unmapped 0x%08X\", addr);\n";
    os << "    return 0;\n";
    os << "}\n";
    os << "\n";

    os << "void IOP_State::io_write32(uint32_t addr, uint32_t val) {\n";
    os << "    if (addr == 0x1F801070) { intc.write_stat(val); return; }\n";
    os << "    if (addr == 0x1F801074) { intc.write_mask(val); return; }\n";
    os << "    if (addr == 0x1F8010F0) { dmac.write_dpcr(val); return; }\n";
    os << "    if (addr == 0x1F8010F4) { dmac.write_dicr(val); return; }\n";
    os << "    if (addr >= 0x1F801080 && addr < 0x1F8010F0) {\n";
    os << "        int ch = (addr - 0x1F801080) / 0x10;\n";
    os << "        int off = (addr - 0x1F801080) % 0x10;\n";
    os << "        if (ch < IOP_DMAC::CHANNEL_COUNT) {\n";
    os << "            if (off == 0x0) dmac.write_ch_base(ch, val);\n";
    os << "            else if (off == 0x4) dmac.write_ch_block(ch, val);\n";
    os << "            else if (off == 0x8) dmac.write_ch_ctrl(ch, val);\n";
    os << "        }\n";
    os << "        return;\n";
    os << "    }\n";
    os << "    if (addr == 0x1F8010C0) { sif.smcom = val; return; }\n";
    os << "    if (addr == 0x1F8010C4) { sif.mscom = val; return; }\n";
    os << "    if (addr == 0x1F8010C8) { sif.smgs = val; return; }\n";
    os << "    if (addr == 0x1F8010CC) { sif.mgs = val; return; }\n";
    os << "    if (addr == 0x1F8010D0) { sif.sbus_ctl = val; return; }\n";
    os << "    if (addr == 0x1F8010D4) { sif.sbus_sync = val; return; }\n";
    os << "    if (addr == 0x1F8010D8) { sif.sbus_cvt = val; return; }\n";
    os << "    if (addr == 0x1F8010E0) { sif.ifc = val; return; }\n";
    os << "    if (addr == 0x1F8010E4) { sif.iff = val; return; }\n";
    os << "    if (addr == 0x1F8010E8) { sif.sif0_msk = val; return; }\n";
    os << "    if (addr >= 0x1F801100 && addr < 0x1F801110) {\n";
    os << "        int t = (addr - 0x1F801100) / 4;\n";
    os << "        if (t < 3) timer_count[t] = val;\n";
    os << "        return;\n";
    os << "    }\n";
    os << "    if (addr >= 0x1F801110 && addr < 0x1F801120) {\n";
    os << "        int t = (addr - 0x1F801110) / 4;\n";
    os << "        if (t < 3) timer_target[t] = val;\n";
    os << "        return;\n";
    os << "    }\n";
    os << "    if (addr >= 0x1F801120 && addr < 0x1F801130) {\n";
    os << "        int t = (addr - 0x1F801120) / 4;\n";
    os << "        if (t < 3) timer_mode[t] = val;\n";
    os << "        return;\n";
    os << "    }\n";
    os << "    if (addr == 0x1F801040) {\n";
    os << "        if (sio2_stat & 0x04) {\n";
    os << "            memmove(sio2_fifo + 1, sio2_fifo, sizeof(sio2_fifo) - 4);\n";
    os << "            sio2_fifo[0] = val;\n";
    os << "        }\n";
    os << "        return;\n";
    os << "    }\n";
    os << "    if (addr == 0x1F801044) { sio2_stat = val; return; }\n";
    os << "    LOGW(\"IOP io_write32: unmapped 0x%08X = 0x%08X\", addr, val);\n";
    os << "}\n";

    return os.str();
}

