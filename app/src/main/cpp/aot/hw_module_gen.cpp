HWModule HW_ModuleGen::generate_ee_module(const ELF_Analyzer& analyzer) {
    HWModule mod;
    mod.name = "ee";
    mod.header_code = generate_ee_header(analyzer);
    mod.source_code = generate_ee_source(analyzer);
    return mod;
}

