    os << "\n";
    os << "void VU_State::cop2_sq(int vs, uint16_t addr) {\n";
    os << "    addr &= (VU_DATA_MEM_MAX - 16);\n";
    os << "    memcpy(data_mem + addr, vf[vs].f, 16);\n";
    os << "    LOGI(\"VU COP2 SQ vs=%d addr=0x%04X\", vs, addr);\n";
    os << "}\n";
