#include "hw_module_gen.h"
#include <android/log.h>
#include <sys/stat.h>
#include <sys/types.h>
#include <fstream>
#include <sstream>
#include <cstring>

#define LOG_TAG "HWModuleGen"
#define LOGI(...) __android_log_print(ANDROID_LOG_INFO, LOG_TAG, __VA_ARGS__)
#define LOGE(...) __android_log_print(ANDROID_LOG_ERROR, LOG_TAG, __VA_ARGS__)

static void ensure_parent_dirs(const std::string& path) {
    size_t pos = 0;
    while ((pos = path.find('/', pos + 1)) != std::string::npos) {
        mkdir(path.substr(0, pos).c_str(), 0755);
    }
}

void HW_ModuleGen::write_file(const std::string& path, const std::string& content) {
    ensure_parent_dirs(path);
    std::ofstream ofs(path, std::ios::binary);
    if (!ofs.is_open()) {
        LOGE("Failed to open file for writing: %s", path.c_str());
        return;
    }
    ofs.write(content.data(), content.size());
    ofs.close();
    LOGI("Wrote %zu bytes to %s", content.size(), path.c_str());
}

void HW_ModuleGen::generate_all(const ELF_Analyzer& analyzer, const std::string& out_dir) {
    LOGI("Generating all HW modules to %s", out_dir.c_str());
    mkdir(out_dir.c_str(), 0755);

    auto ee = generate_ee_module(analyzer);
    write_file(out_dir + "/ee_state.h", ee.header_code);
    write_file(out_dir + "/ee_state.cpp", ee.source_code);

    auto iop = generate_iop_module(analyzer);
    write_file(out_dir + "/iop_state.h", iop.header_code);
    write_file(out_dir + "/iop_state.cpp", iop.source_code);

    auto gs = generate_gs_module(analyzer);
    write_file(out_dir + "/gs_renderer.h", gs.header_code);
    write_file(out_dir + "/gs_renderer.cpp", gs.source_code);

    auto dma = generate_dma_module(analyzer);
    write_file(out_dir + "/dma_controller.h", dma.header_code);
    write_file(out_dir + "/dma_controller.cpp", dma.source_code);

    auto vu = generate_vu_module(analyzer);
    write_file(out_dir + "/vu_processor.h", vu.header_code);
    write_file(out_dir + "/vu_processor.cpp", vu.source_code);

    LOGI("All HW modules generated successfully");
}

